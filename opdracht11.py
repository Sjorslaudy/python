def maal(getal_1, getal_2):  
    antwoord = getal_1 * getal_2
    return antwoord

getal_1 = float(input("Voer het eerste getal in: ")) # Variabel genaamd getal_1 krijgt input (voer het eerste getal in)
getal_2 = float(input("Voer het tweede getal in: ")) # Variabel genaamd getal_2 krijgt input (voer het tweede getal in)

antwoord = maal(getal_1,getal_2)

print(antwoord) # geef antwoord van variabel(antwoord)